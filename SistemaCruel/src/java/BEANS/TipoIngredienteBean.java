package BEANS;

public class TipoIngredienteBean {
    
    private int idTipoIngrediente;
    private String nomeTipoIngrediente;
    
    public TipoIngredienteBean(){
    }
    
    public int getIdTipoIngrediente() {
        return idTipoIngrediente;
    }

    public void setIdTipoIngrediente(int idTipoIngrediente) {
        this.idTipoIngrediente = idTipoIngrediente;
    }

    public String getNomeTipoIngrediente() {
        return nomeTipoIngrediente;
    }

    public void setNomeTipoIngrediente(String nomeTipoIngrediente) {
        this.nomeTipoIngrediente = nomeTipoIngrediente;
    }
    
}
